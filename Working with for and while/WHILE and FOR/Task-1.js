// Выведите столбец чисел от 1 до 100.  Решите эти задачи сначала через цикл while, а затем через цикл for.

let number = 0
while (number < 100) {
    number++
    console.log(number)
}


console.log("РАЗДЕЛИТЕЛЬ---------------------------------------")

for (number = 1; number <= 100; number++) {
    console.log(number)
}