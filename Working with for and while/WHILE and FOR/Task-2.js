// Выведите столбец чисел от 11 до 33. Решите эти задачи сначала через цикл while, а затем через цикл for.

let number = 10
while (number < 33) {
    number++
    console.log(number)
}


console.log("РАЗДЕЛИТЕЛЬ---------------------------------------")

for (number = 11; number <= 33; number++) {
    console.log(number)
}